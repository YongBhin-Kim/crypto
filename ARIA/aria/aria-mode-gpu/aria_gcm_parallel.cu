    res[7]=temp[1]^temp[3]^temp[6]^temp[8]^temp[11]^temp[12]^temp[13];

    res[8]=temp[0]^temp[1]^temp[4]^temp[7]^temp[10]^temp[13]^temp[15];
    res[9]=temp[0]^temp[1]^temp[5]^temp[6]^temp[11]^temp[12]^temp[14];
